#include <stdio.h>
#include <math.h>
int main(){
    float a, b, c, s, area;
    printf("Type the sides of the triangle\t");
    scanf("%f %f %f", &a, &b, &c);
    s = (a + b + c)/2;
    area = sqrt(s* (s-a) * (s-b) * (s-c));

    printf("The area is %f", area);
    return 0;





}
